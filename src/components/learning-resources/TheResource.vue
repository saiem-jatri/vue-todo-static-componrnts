<template>
  <base-card>
  <div class="flex gap-x-4">
    <base-button @click="setSelectedTab('stored-resources')" :mode="storedResButtonMode"
      >Stored Resources</base-button
    >
    <base-button @click="setSelectedTab('add-resource')" :mode="addResButtonMode"
      >Add Resources</base-button
    >
  </div>

  </base-card>

  <component :is="selectedTab"></component>
</template>

<script>
import StoredResources from "./StoredResources.vue";
import AddResource from "./AddResource.vue";
export default {
  components: {
    StoredResources,
    AddResource,
  },
  data() {
    return {
      selectedTab: "stored-resources",
      storedResources: [
        {
          id: "official-guide",
          title: "Official Guide",
          description: "The official vue.js documentation",
          link: "https://vuejs.org",
        },
        {
          id: "google",
          title: "Google",
          description: "The official vue.js documentation",
          link: "https://google.com",
        },
      ],
    };
  },
  provide() {
    return {
      resources: this.storedResources,
      addResource:this.addResource,
      deleteResource:this.removeResource
    };
  },
  methods: {
    setSelectedTab(tab) {
      this.selectedTab = tab;
    },
    addResource(id,title,desc,url){
        const newResource = {
            id: new Date().toISOString(),
            title:title,
            description:desc,
            link:url
        }
        this.storedResources.unshift(newResource);
        this.selectedTab = 'stored-resources';

    },
    removeResource(resId){
        const reIndex=this.storedResources.findIndex(res => res.id === resId );
        this.storedResources.splice(reIndex,1)
    }
  },
  computed:{
storedResButtonMode(){
    return this.selectedTab === 'stored-resources' ? null : 'flat';
},
addResButtonMode(){
    return this.selectedTab === 'add-resource' ? null : 'flat';
},
  }
};
</script>